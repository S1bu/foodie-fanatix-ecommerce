function add_to_cart(starters) {
    cart.push({
        id: starters.id,
        product: starters.product,
        Image: starters.Image,
        price: starters.price,
        category: starters.category
    });
    function cart_count() {
        let cart_length = cart.length;
        cart_target.innerHTML = `
        ${cart_length}
        `;
    }

    cart_count();
}

